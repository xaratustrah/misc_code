public class Transistor {

	private double gm;

	private double rin;

	private double rout;

	public void setGm(double gm) {
		this.gm = gm;
	}

	public double getGm() {
		return gm;
	}

	public void setRin(double rin) {
		this.rin = rin;
	}

	public double getRin() {
		return rin;
	}

	public void setRout(double rout) {
		this.rout = rout;
	}

	public double getRout() {
		return rout;
	}
}
